import math

x = 3.7
y = 4


print("pierwiastek kwadratowy z x:",math.sqrt(x))
print('potęga x^y:',math.pow(x,y))
print('Pierwiastek y-tego stopnia z x:',math.pow(x,1/y))
print('Pole koła o promieniu y:',math.pow(math.pi,2)*y)
print('Silnia y:',math.factorial(y))
print('Największa możliwa liczba całkowita, mniejsza bądź równa x:',math.floor(x))
